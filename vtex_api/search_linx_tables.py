def search_table(table):
	print(table)
	params = {
		'table': table,
		'where': '1=1',
		'top': 'top 1'
	}

	query_template = """
		SELECT %(top)s
			*
		FROM %(table)s
		WHERE %(where)s
	""" 

	query = query_template % params

	rows = dc.select(query, dict_format=True, strip=True)

	if not rows:
		# empty table
		return

	columns = [column[0] for column in dc.cursor.description]

	searched_values = ['000006726', '6726', '35190107400225000184550020000067261737732376']
	or_clauses = []
	for column in columns:
		for value in searched_values:
			or_clauses.append("(%s like '%s')" % (column, value))

	params['where'] = ' OR '.join(or_clauses)
	params['top'] = ''

	query = query_template % params
	# import pdb; pdb.set_trace()

